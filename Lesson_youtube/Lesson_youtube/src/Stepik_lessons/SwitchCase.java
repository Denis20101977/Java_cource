package Stepik_lessons;

public class SwitchCase {
    public static void main(String[] args) {
        String nameOfMonth = "Декабрь";
        switch (nameOfMonth) {
            case "Декабрь":
            case "Январь":
            case "Февраль":
                System.out.println("Зима");
                break;

        }

    }
}

